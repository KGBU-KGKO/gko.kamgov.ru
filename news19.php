		<h1 class="title">Коротко об оценке объектов незавершенного строительства.</h1>
		<p>На территории Камчатского края в 2020 году проводится оценка в отношении 998 объектов незавершенного строительства (далее - ОНС).</p>
		<p>ОНС проводится по методике определения кадастровой стоимости объектов недвижимости, введенных в эксплуатацию. При этом в расчетах учитывается процент готовности ОНС, дата начала строительного производства (выдачи разрешения на строительство) и дата приостановки строительно-монтажных работ, а также наличие мероприятий по консервации ОНС.</p>
		<p>Процент готовности ОНС (при отсутствии документального подтверждения) рассчитывается как отношение общей продолжительности срока, прошедшего с получения разрешения на строительство (или начала строительства) к общему сроку проектно-изыскательских работ, согласований и строительно-монтажных работ (нормативного или типичного). В случае если отношение равно или превышает 100 процентов, объект недвижимости <b>оценивается как готовый к использованию.</b></p>
		<p>Для расчета кадастровой стоимости необходимо учитывать фактический (реальный) процент готовности ОНС, сведения о котором содержаться в техническом плане объекта незавершенного строительства.</p>
