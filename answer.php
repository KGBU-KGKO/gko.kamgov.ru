				<h2>Требования к содержанию заявления (обращения) о предоставлении разъяснений</h2>
				<p>Обращение о предоставлении разъяснений в обязательном порядке должно содержать:</p>
				<ul>
					<li>фамилию, имя и отчество (последнее - при наличии), адрес места жительства физического лица, полное наименование, местонахождение юридического лица, номер контактного телефона, адрес электронной почты (при наличии) лица, подавшего обращение о предоставлении разъяснений;</li>
					<li>кадастровый номер и (или) адрес объекта недвижимости.</li>
				</ul>
				<p><b>Заявитель в <u>обязательном</u> порядке должен приложить Выписку из Единого государственного реестра недвижимости к Заявлению для подтверждения прав на объект недвижимости (для актуальности отображения информации Выписка из ЕГРН должна быть предоставлена сроком действия не более 30 рабочих дней)</b></p>
				<p>Обращения о предоставлении разъяснений, не соответствующие требованиям, установленным законодательством о государственной кадастровой оценке, не подлежат рассмотрению.</p>
