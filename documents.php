<script>
$(function($)
{
	var opened;
	
	function open()
	{
		var t = $(this), target = $(t.data('target'));

		$('body').css('overflow', 'hidden');
		target.fadeIn();
		
		opened = target;
	}
	function close()
	{
		//var target = opened.parents('.b-documents-popup');
		$('body').css('overflow', 'visible');
		opened.fadeOut();
	}
	
	$('.b-documents-document').click(open);	
	$('.b-documents-popup-close').click(close);	
	$(document).keydown(function(e)
	{
		if ( e.which == 27 ) close();
	})
	
	$('.b-documents-popup').click(close);
	$('.b-documents-popup-content').click(function(e)
	{
		e.stopPropagation();
	})
})
</script>
