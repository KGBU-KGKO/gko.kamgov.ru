		<h1 class="title">Круговорот оценки в экономической жизни недвижимости.</h1>
		<p>Оценка представляется как последовательность действий, определения ценность объекта. В оценочной практике сложилось два вида оценки: массовая и индивидуальная. Индивидуальная оценка в основном проводится по необходимости владельца недвижимости, а массовая по требованию законодательства о государственной кадастровой оценке.</p>
		<p>Круговорот массовой оценки начинается с этапа подготовки, в ходе которой изучается рынок недвижимости, характеристики недвижимости и его использование. По результату изучения формируется математическая модель ценности объекта.</p>
		<p>Модель ценности объекта размещают, как результат оценки, в Государственном фонде данных для всеобщего обозрения. При этом заинтересованное лицо, ознакомившись с моделью оценки, может предупредить об ошибке, тем самым повлиять на ее справедливость.</p>
		<p>Процесс государственной кадастровой оценки не оканчивается утверждением итогового результата ценности объекта, а продолжается в непрерывном мониторинге за изменением факторов, влияющих на ценность объекта, и переоценивает результаты по мере необходимости, но не чаще одного раза в три года и не реже одного раза в пять лет.</p>
